# scope:
#   this script is applied to non-RE-cleaved fragments, i.e., a non-ligFree library,
#   to skip non-applicable actions/index levels in the regular index_fragments.pl
# action:
#   describe the SV paths of all reads
#   use a fragment index to count unique paths
#   work is parallelized by chromosome for speed gains and sorting efficiency
#   this script is applicable to all sequencing platforms
#   SV paths are described by:
#       refPos1_5 = the 5'-most fragment coordinate plus clipping on the indexed chrom/strand
#       isEndToEnd = 0|1 indicating whether refPos1_3 represents the end of the sequenced insert
#           when isEndToEnd == 1 for an unpaired read, the last distalNode is redundant with refPos1_3
#       distalNodes = a colon-delimited list of more distal/3' nodes (just the 3'-most node if no SV in read)
#       junctions = a colon-delimited list of SV junctions between the nodes ("*" if no SV in read)
#       refPos1_3 = the 3'-most fragment coordinate plus clipping on the chromosome of the last alignment/distal node
#       readHasSv = 0|1 indicating whether this read retained a SV AFTER haplotype adjustment by `align genotype`
#   where paths are indexed as:
#       chromIndex1,refPos1_5,strandIndex0, where
#           chromIndex1  = 1-based index of ordered chromosomes
#           strandIndex0 = 0 for top strand, 1 for bottom strand sequence
#   a distal/3' junction node is described as:
#       chromIndex1,refPos1,strandIndex0, where
#           refPos1 = 1-based coordinate of the last aligned base in the chromosome
#   and an SV junction is described by read-dependent properties that can't be derived from nodes:
#       alignmentOffset,jxnBases, where
#           alignmentOffset = number of non-reference bases found at junction (negative if microhomology)
#           jxnBases = the inserted or microhomologous bases (omitted for long ONT sequences)
#   junctions were already split upstream at low quality alignments and chimeras
#   junctions and associated distal nodes are omitted here, as if they didn't exist,
#       when blockN of the two flanking alignments is the same, i.e., the junction failed traversalDelta checks
#       since the most likely true path was contiguous across that (portion of the) read
#   a hash is used to track the unique paths encountered for each unique 5' end as:
#       isEndToEnd::distalNodes::junctions::refPos1_3::readHasSv::matchingHaps
#   indexing occurs with respect to read strand, so reads are NOT aggregated by strand
#       i.e., a variant sequenced in two strand orientations will be found on at least two indexed lines
#       no such strand aggregation is performed downstream since:
#           overlapping paired reads were previously merged to prevent redundancy in the overlap region
#           two single-end reads rarely arise from two strands of the same source duplex
#       thus, the potential for non-independent variant calls on two strands is considered small
#       however, multiple independent indexed lines may exist for a clonal variant if sequencing errors give distinct hash keys
#   indexing is an aggregating action, so read/event identifiers are lost in the index
#   to allow indexed SV paths to be correlated back to source reads, especially for ONT duplex purging
#       a list is saved of all pre-aggregation SV paths with their associated QNAME and ONT channel
#       as with the index, different paths in the list may be non-identical but with the same SV due to sequencing errors
# input:
#   SITE_SAM files generated by `analyze fragments`, one file per chromosome
# output:
#   alignment-level files, with event counts, used for generating fragment maps:
#       FIRST_ALIGNMENTS    = the single alignment at the 5' end of a read
#                               sorted by coordinate by this script
#       DISTAL_ALIGNMENTS   = the alignment(s) after SV junction(s) in the read
#                               sorted by coordinate after downstream aggregation to genome-level files
#                               last distal alignment carries information on the distance from refPos1_2 to refPos1_3
#           all alignment files carry information on three nodes (even if empty==0)
#               refPos1_5 = the 5'-most base in the alignment (node1)
#               refPos1_2 = the 3'-most base in the alignment (node2)
#               refPos1_3 = the 3'-most base in the RE fragment (0 if not known, not applicable, not projected)
#           alignment nodes are in 5' to 3' order on the read, i.e., refPos1_5 is > refPos1_2 when strandIndex0 == 1
#   junction-level files, used for establishing SV uniqueness and zygosity:
#       JUNCTION_SOURCES    = one line per pre-aggregation junction with source QNAME and ONT channel
#       UNIQUE_JUNCTIONS    = one line per post-aggregation junction with event counts
#           junction nodes are sorted such that:
#               SVs encountered on different strands have the same node1 and node2 order
#               column strandIndex0 indicates the source strand/orientation, i.e., is true==1 when flipped relative to the read
#           junction nodes are not yet aggregated for fuzzy-logic proximity matching or ONT duplex purging
#   note that :
#       most alignments are in FIRST_ALIGNMENTS on all platforms
#       DISTAL_ALIGNMENTS all come from SV molecules
#   all output files are, at least initially, one file per _indexing_ chromosome
#       only entries in FIRST_ALIGNMENTS are guaranteed to reside on the indexing chromosome
#       all other files are aggregated into coordinate-sorted genome-level files in the next action step

